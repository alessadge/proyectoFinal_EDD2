    public ArrayList<Campo> llenarCampos() {
        ArrayList<Campo> temp = new ArrayList();
        for (int i = 0; i < campos.size(); i++) {
            Campo c = new Campo();
            c.setNombre(campos.get(i).getNombre());
            temp.add(c);
        }
        return temp;
    }

    public void write(String pat) throws FileNotFoundException, IOException {
        // FileWriter entrada = new FileWriter("C:\\Users\\adgri_001\\Desktop\\Personas.txt");
        FileWriter entrada = new FileWriter(pat);
        BufferedWriter buff = new BufferedWriter(entrada);

        if (metadata == null) {
            System.out.println("Error no hay ningun archivo que guardar");
        } else {
            buff.write(metadata);
            buff.write("|");
            buff.newLine();

            for (int i = 0; i < campos.size(); i++) {
                buff.write(campos.get(i).getNombre() + ",");
            }
            buff.write("&");
            buff.newLine();
            for (int i = 0; i < registros.size(); i++) {
                buff.write(i + "");
                for (int j = 0; j < registros.get(i).getCampos().size(); j++) {
                    buff.write(registros.get(i).getCampos().get(j).getContenido() + ",");
                }
                buff.newLine();
            }
            buff.write("~");

        }
        buff.flush();
        buff.close();
    }

    public String leer(String p) {
        String acum = "";
        try {
            //FileReader entrada = new FileReader("C:\\Users\\adgri_001\\Desktop\\Personas.txt");
            FileReader entrada = new FileReader(p);

            BufferedReader mibuffer = new BufferedReader(entrada);

            String linea = "";

            while (linea != null) {
                linea = mibuffer.readLine();

                acum += linea;
            }
            entrada.close();
        } catch (IOException e) {
            System.out.println("No se ha encontrado el archivo");
        }

        return acum;
    }

    public Registro agregarCampos(Registro r, String[] s) {
        r = new Registro();
        for (int i = 0; i < s.length; i++) {
            r.getCampos().add(new Campo(s[i]));
        }
        return r;
    }

    public void Save() {
        FileOutputStream fw = null;
        ObjectOutputStream bw = null;
        try {
            fw = new FileOutputStream(archivoGuardar);
            bw = new ObjectOutputStream(fw);
            bw.writeObject(archivo.getNombre() + "\n");
            for (Campo t : campos) {
                bw.writeObject(t + ",");
            }
            bw.writeObject("\n");
            String aux = "";
            for (int i = 0; i < registros.size(); i++) {
                aux += i + "-)";
                for (int j = 0; j < registros.get(i).getCampos().size(); j++) {
                    aux += registros.get(i).getCampos().get(j).getContenido() + ",";
                }
                aux += "\n";
            }
            bw.writeObject(aux);

            bw.flush();
            bw.close();
            fw.close();
        } catch (Exception e) {

        } finally {
            try {
                bw.close();
                fw.close();
            } catch (Exception e) {
            }
        }

    }

