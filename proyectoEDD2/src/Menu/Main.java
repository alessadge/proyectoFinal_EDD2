    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) tabla_campo.getModel();
        Campo aux = new Campo();
        int verificar = 0;
        String s;
        for (int i = 0; i < modelo.getRowCount(); i++) {
            //Campo aux = new Campo();

            for (int j = 0; j < modelo.getColumnCount(); j++) {
                s = (String) modelo.getValueAt(i, j);
                verificar = 0;
                if (campos.size() == 0) {
                    aux = new Campo();
                    aux.setNombre(s);
                    campos.add(aux);
                } else {
                    for (int k = 0; k < campos.size(); k++) {
                        if (campos.get(k).getNombre().equals(s)) {
                            verificar = 1;
                        }
                    }
                    if (verificar == 0) {
                        aux = new Campo();
                        aux.setNombre(s);
                        campos.add(aux);
                    }
                }

            }

            JOptionPane.showConfirmDialog(null, "Se agrego exitosamente!");

        }
    }//GEN-LAST:event_jButton7ActionPerformed

